package Entity;


public class Banda {

    private float lunghezza;

    //constructor
    public Banda(float lunghezza) {
        this.lunghezza = lunghezza;
    }


    //getter and setter
    public float getLunghezza() {
        return lunghezza;
    }
    public void setLunghezza(float lunghezza) {
        this.lunghezza = lunghezza;
    }

}
